<h3>6 Cara Buat Laptop Lemot Jadi Ngebut</h3>
<br>
<p> Laptop lemot sering mengganggu kerja atau belajar, apalagi jika sering hang. Rasanya mau ganti laptop saja, tapi masih harus berhemat untuk anggaran lain.
Merangkum dari Panda Security, Rabu (2/6/2021) begini tipsnya agar laptop bekerja lebih cepat.

Cara mengatasi laptop lemot:
1. Cek ruang hard disk
Sangat penting untuk memastikan ada ruang bebas 15% di hard disk. Jika kamu mendapat hard disk mulai penuh, hapus beberapa program dan file bisa jadi pilihan untuk meningkatkan kecepatan.

2. Tutup tab yang tidak terpakai
Jika kamu bekerja dengan proyek besar, maka sangat mungkin kamu membuka banyak tab dan lupa untuk menutup tab yang tidak terpakai. Padahal menutup tab tersebut bisa membuat kerja laptop lebih cepat. Jangan lupa refresh browser.
